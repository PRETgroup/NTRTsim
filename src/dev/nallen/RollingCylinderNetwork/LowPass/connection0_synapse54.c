#include "connection0_synapse54.h"

// connection0_synapse54 Initialisation function
void Connection0Synapse54Init(Connection0Synapse54* me) {
    // Initialise State
    me->state = CONNECTION0_SYNAPSE54_L;

    // Initialise Outputs
    me->out = 0.0;

    // Initialise Internal Variables
    me->alpha = 0.001 / (0.01 + 0.001);
}

// connection0_synapse54 Execution function
void Connection0Synapse54Run(Connection0Synapse54* me) {
    // Create intermediary variables
    enum Connection0Synapse54States state_u = me->state;
    double out_u = me->out;

    double alpha_u = me->alpha;


    // Run the state machine for transition logic
    // This will only be inter-location transitions, with intra-location transitions happening later
    switch(me->state) {
        case CONNECTION0_SYNAPSE54_L: // Logic for state l
            break;
    }

    // Update from intermediary variables
    me->state = state_u;
    me->out = out_u;

    me->alpha = alpha_u;


    // Intra-location logic for every state
    switch(me->state) {
        case CONNECTION0_SYNAPSE54_L: // Intra-location logic for state l
            out_u = me->alpha * me->in + (1.0 - me->alpha) * me->out;

            break;
    }

    // Update from intermediary variables
    me->out = out_u;

    me->alpha = alpha_u;

}